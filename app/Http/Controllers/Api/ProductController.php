     * @param \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $name = $request->name;
        $slug = $request->slug;
        $description = $request->description;
        $sku = $request->sku;
        $price = $request->price;

        return Products::create([
            'name' => $name,
            'slug' => $slug,
            'description' => $description,
            'sku' => $sku,
            'price' => $price
        ]);

    }

